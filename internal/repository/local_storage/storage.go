type FeedStorage struct {
	groupPosts []entities.Post
	userPosts  []entities.Post
}

func NewFeedStorage() *FeedStorage {
	return &FeedStorage{
		groupPosts: mockGroupPosts,
		userPosts:  mockUserPosts,
	}
}

func (storage *FeedStorage) GetFriendsPosts(user *entities.User, filterDateTime time.Time, postsNumber uint) ([]entities.Post, error) {
	return storage.userPosts, nil
}

func (storage *FeedStorage) GetGroupsPosts(user *entities.User, filterDateTime time.Time, postsNumber uint) ([]entities.Post, error) {
	return storage.groupPosts, nil
}

type UserStorage struct {
	user map[string]*entities.User
}

func NewUserStorage() *UserStorage {
	return &UserStorage{
		user: mockUsers,
	}
}

func (lc *UserStorage) GetUserById(id uint) (*entities.User, error) {
	return nil, nil
}

func (lc *UserStorage) GetUserByEmail(email string) (*entities.User, error) {
	user := lc.user[email]
	if user == nil {
		return nil, errors.New("user not found")
	}
	return user, nil
}

func (lc *UserStorage) GetUserFriends(user *entities.User) ([]*entities.User, error) {
	return nil, nil
}

func (lc *UserStorage) CreateUser(user *entities.User) (*entities.User, error) {
	if lc.user[user.Email] != nil {
		return nil, errors.New("user already exists")
	}
	lc.user[user.Email] = user
	return user, nil
}

